public class Vehicle {
    private String id;
    private String type;

    public Vehicle(String id, String type) {
        this.id = id;
        this.type = type;
    }

    public String getId() {
        return this.id;
    }

    public String getType() {
        return this.type;
    }

    public void setId() {
        this.id = id;
    }

    public void setType() {
        this.type = type;
    }
}
